<script>
    lucide.createIcons();

    function openMapModal(embedUrl, profileUrl) {
        const iframe = document.getElementById('mapIframe');
        const modal = document.getElementById('mapModal');
        const directionsBtn = document.getElementById('modalDirectionsBtn');
        const profileBtn = document.getElementById('modalProfileBtn');
        
        iframe.src = embedUrl;
        
        // Update the action buttons with the actual raw Google Maps link
        if(profileUrl) {
            directionsBtn.href = profileUrl + "/dir/"; // Appending /dir/ forces Maps into directions mode
            profileBtn.href = profileUrl;
        }

        modal.classList.remove('hidden');
        modal.classList.add('flex');
        document.body.style.overflow = 'hidden';
    }

    function closeMapModal() {
        const modal = document.getElementById('mapModal');
        const iframe = document.getElementById('mapIframe');
        modal.classList.add('hidden');
        modal.classList.remove('flex');
        iframe.src = '';
        document.body.style.overflow = '';
    }
</script>

